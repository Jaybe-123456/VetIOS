/**
 * @vetios/ai-core — Inference Orchestrator (Refactor v2)
 *
 * Replaces runInference() in apps/web/lib/ai/provider.ts.
 * Drop-in compatible: accepts the same InferenceInput shape,
 * returns output_payload that goes directly into ai_inference_events.
 *
 * Pipeline:
 *   1. Input normalisation + symptom extraction
 *   2. Contradiction detection (Fix 4) — gates abstention
 *   3. LLM diagnosis model call (Fix 5: what is it?)
 *   4. ML severity model call (Fix 5: how dangerous?)
 *   5. Emergency rule override layer (Fix 2 + 3)
 *   6. Differential re-ranking (GDV promotion if rule fired)
 *   7. Confidence penalty for contradictions and overrides
 *   8. Telemetry emission
 *   9. Output assembly (backward-compatible schema v2)
 *
 * Backward compatibility guarantees:
 *   - inference_event_id: unchanged (generated by logInference, not here)
 *   - output_payload.analysis: preserved
 *   - output_payload.recommendations: preserved
 *   - output_payload.confidence_score: preserved
 *   - All new fields are additive (schema_version: '2.0' signals v2)
 */

