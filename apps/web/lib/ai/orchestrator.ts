// ─── Provider Config ──────────────────────────────────────────────────────────

interface LLMConfig {
    apiKey:    string;
    baseUrl:   string;
    model:     string;
    timeoutMs: number;
}

function getLLMConfig(modelOverride?: string): LLMConfig {
    const key = process.env.OPENAI_API_KEY || process.env.AI_PROVIDER_API_KEY;
    if (!key) throw new Error('Missing AI provider key: set OPENAI_API_KEY or AI_PROVIDER_API_KEY.');
    return {
        apiKey:    key,
        baseUrl:   process.env.AI_PROVIDER_BASE_URL || 'https://api.openai.com/v1',
        model:     modelOverride || process.env.AI_PROVIDER_DEFAULT_MODEL || 'gpt-4o-mini',
        timeoutMs: 45_000,
    };
}

// ─── ML Severity Client ───────────────────────────────────────────────────────

interface MLSeverityResponse {
    risk_score:      number;
    confidence:      number;
    abstain:         boolean;
    model_version:   string;
    _fallback?:      boolean;
}

async function callMLSeverityModel(
    species: string,
    decisionCount: number,
    overrideCount: number,
): Promise<MLSeverityResponse> {
    const mlUrl = process.env.ML_SERVER_URL || 'http://localhost:8000';
    const timeoutMs = Number(process.env.ML_TIMEOUT_MS) || 5000;

    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), timeoutMs);

    try {
        const res = await fetch(`${mlUrl}/predict`, {
            method:  'POST',
            headers: { 'Content-Type': 'application/json' },
            body:    JSON.stringify({ decision_count: decisionCount, override_count: overrideCount, species }),
            signal:  controller.signal,
        });
        clearTimeout(timer);
        if (!res.ok) throw new Error(`ML server ${res.status}`);
        return await res.json() as MLSeverityResponse;
    } catch {
        clearTimeout(timer);
        // Graceful fallback — never block inference on ML unavailability
        return { risk_score: 0.5, confidence: 0.0, abstain: true, model_version: 'fallback', _fallback: true };
    }
}

